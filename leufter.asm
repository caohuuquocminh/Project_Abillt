#! mrasm
.ORG 0
	LD		SP,0xEF
	CLR		R0
	CLR 	R1
LOOP:
	CALL 	RECHNUNG
	LD		R1,(0xFE)
	CMP		R0,R1
	JNC 	VERGLEICH2
	LD		R1,0x00
	JR		AUSGABE
VERGLEICH2:
	LD		R1,(0xFF)
	CMP		R0,R1
	JNC		VERGLEICH3
	LD		R1,(0xFD)
	JR		AUSGABE
VERGLEICH3:
	LD		R1,(0XFC)
AUSGABE:
	ST		(0xFE),R1
	ST		(0xF0),R1
	ST		(0xFF),R0
	JR		LOOP

;Unterprogramm

RECHNUNG:
	CLR		R0
	LD		R1,0b10000000
RECHNUNGSLOOP:
	OR		R0,R1
	ST		(0xF1),R0
	BIT		(0xF1),0b00010000
	JZC		HOCH
	SUB		R0,R1
HOCH:
	LSR		R1
	JZC		RECHNUNGSLOOP
	RET	