#! mrasm
.ORG 0
	LD		SP,0xEF
	CLR		R0
	CLR 	R1
LOOP:
	CALL	RECHNUNG
	ST		(0xFF),R0
	ST		(0xFA),R0
	JR		LOOP
RECHNUNG:
	CLR		R0
	LD		R1,0b10000000
RECHNUNGSLOOP:
	OR		R0,R1
	ST		(0xF1),R0
	BIT		(0xF1),0b00010000
	JZC		HOCH
	SUB		R0,R1
HOCH:
	LSR		R1
	JZC		RECHNUNGSLOOP
	RET	

